public class Exponencial
{

    private double lambda;
    private String nombre;

    public Exponencial()
    {
    }

    public double getLambda()
    {
        return lambda;
    }

    public void setLambda(double lambda)
    {
        this.lambda = lambda;
    }

    public String getNombre()
    {
        return nombre;
    }

    public void setNombre(String nombre)
    {
        this.nombre = nombre;
    }

    

}
