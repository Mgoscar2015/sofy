package distribuciones;

/**
 *
 * @author Carolina
 */
public class Empirica
{

    private double[] r;
    private double[] x;
    private double[] a;
    private String nombre;

    public Empirica()
    {
    }

    public double[] getA()
    {
        return a;
    }

    public void setA(double[] a)
    {
        this.a = a;
    }

    public double[] getR()
    {
        return r;
    }

    public void setR(double[] r)
    {
        this.r = r;
    }

    public double[] getX()
    {
        return x;
    }

    public void setX(double[] x)
    {
        this.x = x;
    }

    public String getNombre()
    {
        return nombre;
    }

    public void setNombre(String nombre)
    {
        this.nombre = nombre;
    }

    

}
