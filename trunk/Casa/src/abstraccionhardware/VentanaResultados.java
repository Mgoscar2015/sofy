    private Perfil buscarPerfil(String nombre)
    {
        Perfil p = new Perfil();
        Iterator it = this.perfiles.iterator();
        while(it.hasNext())
        {
            p = (Perfil)it.next();
            if(p.getNombre().trim().toUpperCase().equals(nombre.trim().toUpperCase()))
                break;
        }
        return p;
    }

